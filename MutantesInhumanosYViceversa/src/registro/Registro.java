    /**
     * Valida el formato de la informacion antes de darla de alta en la BD
     *
     * @return
     */
    private boolean validarInformacion() {

        boolean correcto = true;

        String nick = txt_nombre.getText();
        String email = txt_email.getText();

        char[] pwd = txt_pwd.getPassword();
        char[] confirmPwd = txt_confirm_pwd.getPassword();

        if (nick.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre no puede estar vacío.", "Registro", JOptionPane.ERROR_MESSAGE);
            correcto = false;
        } 
        if (correcto && (email.isEmpty() || !Utilidades.validarEmail(email))) {
            JOptionPane.showMessageDialog(this, "El email no tiene el formato correcto.", "Registro", JOptionPane.ERROR_MESSAGE);
            correcto = false;
        } 

        if (correcto && !Utilidades.validarPwd(pwd, confirmPwd)) {
            JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden.", "Registro", JOptionPane.ERROR_MESSAGE);
            correcto = false;
        } 

        return correcto;
    }

